// var : 더 이상 쓰지 않는다.
// let과 const 가 권장됨.
var name = "이혜원";
console.log(name);

var age = 25;
console.log(age);

let ive = "아이브";
console.log(ive);

// let과 var로 선언하면 값을 추후 변경할 수 있다.

const newJeans = "뉴진스";
console.log(newJeans);

// let과 var 키워드는 선언을 한 후 초기화를 하지 않았을 때 자동으로 undefined가 할당이 된다.
